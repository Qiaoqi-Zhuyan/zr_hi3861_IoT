void init(void)
{
    GpioInit();
     IoSetFunc(WIFI_IOT_IO_NAME_GPIO_13, WIFI_IOT_IO_FUNC_GPIO_13_I2C0_SDA);
    IoSetFunc(WIFI_IOT_IO_NAME_GPIO_14, WIFI_IOT_IO_FUNC_GPIO_14_I2C0_SCL);

    I2cInit(WIFI_IOT_I2C_IDX_0, OLED_I2C_BAUDRATE);
    ssd1306_Init();
}

static void Oled_Task(void *arg)
{
    (void)arg;
    init();
    ssd1306_Fill(White);  
    ssd1306_DrawBitmap(qr_code, sizeof(qr_code));
    ssd1306_UpdateScreen();
}
void oled_task(void)
{
    osThreadAttr_t attr;
    attr.name = "Oled_Task";
    attr.attr_bits = 0U;
    attr.cb_mem = NULL;
    attr.cb_size = 0U;
    attr.stack_mem = NULL;
    attr.stack_size = OLED_TASK_STACK_SIZE;
    attr.priority = osPriorityNormal;
    if (osThreadNew(Oled_Task, NULL, &attr) == NULL)
    {
        printf("[OledDemo] Falied to create OledTask!\n");
    }
}