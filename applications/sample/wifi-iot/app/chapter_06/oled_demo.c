void init(void)
{
    GpioInit();
    OledInit();
}

static void Oled_Task(void *arg)
{
    (void)arg;
    init();
    OledFillScreen(0x00);       // 清屏
    OledShowString(0, 0, "Hello, HarmonyOS", 2);
    sleep(1);

    

    //循环显示3次 
    for (int i = 0; i < 3; i++)
    {
        OledFillScreen(0x00);
        OledSetPosition(0, 0);
        for(int i = 0; i < 48 * 48; i++)
            WriteData(qr_code[i]);
        sleep(1);
    }
}

static void Oled_MainLoop(void)
{
    osThreadAttr_t attr;
    attr.name = "Oled_Task";
    attr.attr_bits = 0U;
    attr.cb_mem = NULL;
    attr.cb_size = 0U;
    attr.stack_mem = NULL;
    attr.stack_size = OLED_TASK_STACK_SIZE;
    attr.priority = osPriorityNormal;
    if (osThreadNew(Oled_Task, NULL, &attr) == NULL)
    {
        printf("[OledDemo] Falied to create OledTask!\n");
    }
}

APP_FEATURE_INIT(Oled_MainLoop);
